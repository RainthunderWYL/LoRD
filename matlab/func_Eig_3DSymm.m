function [L1,L2,L3,funcV1,funcV2,funcV3] = func_Eig_3DSymm(a,b,c,x,y,z)
% M = |x,a,b|
%     |a,y,c|
%     |b,c,z|

% Resize inputs into 1-D array
OriginalSize = size(a);
len = numel(a);
a = reshape(a,[1,len]);
b = reshape(b,[1,len]);
c = reshape(c,[1,len]);
x = reshape(x,[1,len]);
y = reshape(y,[1,len]);
z = reshape(z,[1,len]);

Ltmp = zeros(3,len);

cons1=2.^(2./3).*(54.*a.*b.*c + 9.*a.^2.*x + 9.*b.^2.*x - 18.*c.^2.*x + 2.*x.^3 + ...
        9.*a.^2.*y - 18.*b.^2.*y + 9.*c.^2.*y - 3.*x.^2.*y - 3.*x.*y.^2 + ...
        2.*y.^3 - 18.*a.^2.*z + 9.*b.^2.*z + 9.*c.^2.*z - 3.*x.^2.*z + ...
        12.*x.*y.*z - 3.*y.^2.*z - 3.*x.*z.^2 - 3.*y.*z.^2 + 2.*z.^3 + ...
        sqrt(-4.*(3.*a.^2 + 3.*b.^2 + 3.*c.^2 + x.^2 - x.*y + y.^2 - x.*z - ... 
                   y.*z + z.^2).^3 + (54.*a.*b.*c + 9.*a.^2.*(x + y - 2.*z) + ...
                 9.*b.^2.*(x - 2.*y + z) - (2.*x - y - z).*(9.*c.^2 - x.^2 + ...
                      2.*y.^2 - 5.*y.*z + 2.*z.^2 + x.*(y + z))).^2)).^(1./3);
cons2=(2.*2.^(1./3).*(3.*a.^2 + 3.*b.^2 + 3.*c.^2 + x.^2 - x.*y + y.^2 - x.*z - ...
         y.*z + z.^2))./(54.*a.*b.*c + 9.*a.^2.*x + 9.*b.^2.*x - 18.*c.^2.*x + ...
        2.*x.^3 + 9.*a.^2.*y - 18.*b.^2.*y + 9.*c.^2.*y - 3.*x.^2.*y - ...
        3.*x.*y.^2 + 2.*y.^3 - 18.*a.^2.*z + 9.*b.^2.*z + 9.*c.^2.*z - ...
        3.*x.^2.*z + 12.*x.*y.*z - 3.*y.^2.*z - 3.*x.*z.^2 - 3.*y.*z.^2 + ...
        2.*z.^3 + sqrt(-4.*(3.*a.^2 + 3.*b.^2 + 3.*c.^2 + x.^2 - x.*y + ...
                   y.^2 - x.*z - y.*z + z.^2).^3 + ...
            (54.*a.*b.*c + 9.*a.^2.*(x + y - 2.*z) + 9.*b.^2.*(x - 2.*y + z) - ...
                 (2.*x - y - z).*(9.*c.^2 - x.^2 + 2.*y.^2 - 5.*y.*z + ...
            2.*z.^2 + x.*(y + z))).^2)).^(1./3);
cons3 = x+y+z;

Ltmp(1,:) = real(1./6.*(cons1+cons2+2.*cons3));
Ltmp(2,:) = real(1./12.*((1i*sqrt(3)-1)*cons1-(1+1i*sqrt(3))*cons2+4*cons3));
Ltmp(3,:) = real(1./12.*((-1i*sqrt(3)-1)*cons1-(1-1i*sqrt(3))*cons2+4*cons3));

Ltmp = sort(Ltmp);

L1 = reshape(Ltmp(1,:),OriginalSize);
L2 = reshape(Ltmp(2,:),OriginalSize);
L3 = reshape(Ltmp(3,:),OriginalSize);

funcV1 = @(L,A,B,C,X,Y,Z) (A.*C-B.*(Y-L))./((X-L).*(Y-L)-A.*A);
funcV2 = @(L,A,B,C,X,Y,Z) (A.*B-C.*(X-L))./((X-L).*(Y-L)-A.*A);
funcV3 = @(L,A,B,C,X,Y,Z) ones(size(A));
end